  static List<String> search(String query) {
    if (query.isEmpty) {
      return const [];
    }

    final lowerQuery = query.toLowerCase();

    // First, find exact matches or startsWith matches to prioritize them
    final startsWith = _foods
        .where((food) => food.toLowerCase().startsWith(lowerQuery))
        .toList();

    // Then find contains matches that are NOT in the startsWith list
    final contains = _foods
        .where(
          (food) =>
              food.toLowerCase().contains(lowerQuery) &&
              !food.toLowerCase().startsWith(lowerQuery),
        )
        .toList();

    // Combine them
    return [...startsWith, ...contains];
  }
